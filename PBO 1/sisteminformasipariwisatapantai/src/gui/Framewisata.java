    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        //tabel simpan
        try {
            
            if(txtid_wisata.getText().equals("")){
                JOptionPane.showMessageDialog(this, "id_wisata belum diisi");
                txtid_wisata.requestFocus();
            } else if(txtnama_objek.getText().equals("")){
                JOptionPane.showMessageDialog(this, "nama_objek belum diisi");
                txtnama_objek.requestFocus();
            }else if(txtjumlah_pengunjung.getText().equals("")){
                JOptionPane.showMessageDialog(this, "jumlah_pengunjung belum diisi");
                txtjumlah_pengunjung.requestFocus();
            }else if(txtlayanan.getText().equals("")){
                JOptionPane.showMessageDialog(this, "layanan belum diisi");
                txtlayanan.requestFocus();
            }else if(txtid_lokasi.getText().equals("")){
                JOptionPane.showMessageDialog(this, "id_lokasi belum diisi");
                txtid_lokasi.requestFocus();
            } else if(txtfoto.getText().equals("")){
                JOptionPane.showMessageDialog(this, "foto belum diisi");
                txtfoto.requestFocus();
            } else {
                CRUD.simpanwisata(txtid_wisata.getText(), txtnama_objek.getText(), txtjumlah_pengunjung.getText(), txtlayanan.getText(), txtid_lokasi.getText(), txtfoto.getText());
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

