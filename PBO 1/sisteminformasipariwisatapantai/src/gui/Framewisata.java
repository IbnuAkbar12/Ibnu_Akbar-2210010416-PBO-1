    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        String id_wisata = txtid_wisata.getText();
        try {
            if (!id_wisata.equals("")) {
                ResultSet rs = CRUD.caridatauser(id_wisata);
                if (rs.next()) {
                    String nama_objek = rs.getString("nama_objek");
                    String jumlah_pengunjung = rs.getString("jumlah_pengunjung");
                    String layanan = rs.getString("layanan");
                    String id_lokasi = rs.getString("id_lokasi");
                    String foto = rs.getString("foto");
                    // Tampilkan informasi pada antarmuka pengguna
                    txtnama_objek.setText(nama_objek);
                    txtjumlah_pengunjung.setText(jumlah_pengunjung);
                    txtlayanan.setText(layanan);
                    txtid_lokasi.setText(id_lokasi);
                    txtfoto.setText(foto);
                    JOptionPane.showMessageDialog(this, "Data wisata ditemukan");
                } else {
                    JOptionPane.showMessageDialog(this, "Data wisata tidak ditemukan");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Masukkan id_wisata terlebih dahulu");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

